#!/bin/bash
# WSL Development Environment Setup Script
set -euo pipefail

# в”Җв”Җ Log File в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
LOG_DIR="$HOME/local/logs"
mkdir -p "$LOG_DIR"
LOG_FILE="$LOG_DIR/setup-wsl_$(date '+%Y%m%d_%H%M%S').log"

# Duplicate all output (stdout+stderr) to the log file while keeping terminal output
exec > >(tee -a "$LOG_FILE") 2>&1

# в”Җв”Җ Colors в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
BLUE='\033[0;34m'
CYAN='\033[0;36m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
RED='\033[0;31m'
BOLD='\033[1m'
DIM='\033[2m'
NC='\033[0m'

# Summary tracking
SUMMARY_FILE=$(mktemp)
CURRENT_SECTION=""

add_summary() { echo "$1|$2|$3" >> "$SUMMARY_FILE"; }

log()     { echo -e "  ${BLUE}вҶ’${NC} $*"; }
ok()      { echo -e "  ${GREEN}вң“${NC} $*"; add_summary "вң“" "$CURRENT_SECTION" "$*"; }
warn()    { echo -e "  ${YELLOW}вҡ ${NC}  $*"; add_summary "вҡ " "$CURRENT_SECTION" "$*"; }
die()     { echo -e "  ${RED}вң—${NC} $*"; add_summary "вң—" "$CURRENT_SECTION" "$*"; exit 1; }
section() { echo -e "\n${CYAN}${BOLD}в”Ңв”Җ $* ${NC}${DIM}в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"; CURRENT_SECTION="$*"; }

# в”Җв”Җ Banner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
clear
echo -e "${CYAN}${BOLD}"
echo "  в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"
echo "  в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ"
echo "  в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—"
echo "  в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•”в•җв•җв•қ  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘"
echo "   в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘"
echo "    в•ҡв•җв•җв•җв•қ  в•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•қ  в•ҡв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ"
echo ""
echo "                    в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—"
echo "                    в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘"
echo "                    в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘"
echo "                  В· в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•қ  в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қ"
echo "                    в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ "
echo "                    в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•қ  "
echo -e "${NC}"
echo -e "  ${DIM}Setting up Vicente's Development Environment${NC}"
echo -e "  ${DIM}WSL2 В· Ubuntu В· $(date '+%A, %B %d %Y  %H:%M')${NC}"
echo ""

# в”Җв”Җ WSL Interop в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "WSL Interop"
log "Configuring Windows interop..."
if ! grep -qs '\[interop\]' /etc/wsl.conf; then
    sudo tee -a /etc/wsl.conf <<'EOF'
[interop]
enabled=true
appendWindowsPath=true
EOF
    ok "WSL interop enabled вҖ” run 'wsl --shutdown' from Windows to apply"
else
    warn "Already configured вҖ” skipped"
fi

# в”Җв”Җ .wslconfig (Windows host) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "Windows WSL Resource Limits & Networking"

# Locate the Windows user profile
WIN_USER=$(powershell.exe -NoProfile -Command '$env:USERNAME' 2>/dev/null | tr -d '\r\n')
WSLCONFIG="/mnt/c/Users/${WIN_USER}/.wslconfig"

log "Windows user: ${WIN_USER}"
log "Target file:  ${WSLCONFIG}"
echo ""

# Show current values if file exists
CURRENT_MEM=""
CURRENT_CPU=""
if [ -f "$WSLCONFIG" ]; then
    CURRENT_MEM=$(grep -i '^memory' "$WSLCONFIG" 2>/dev/null | cut -d= -f2 | tr -d ' ' || true)
    CURRENT_CPU=$(grep -i '^processors' "$WSLCONFIG" 2>/dev/null | cut -d= -f2 | tr -d ' ' || true)
fi

# Prompt for memory
if [ -n "$CURRENT_MEM" ]; then
    echo -e "  ${BOLD}Memory limit${NC} ${DIM}(current: ${CURRENT_MEM})${NC}"
else
    echo -e "  ${BOLD}Memory limit${NC} ${DIM}(e.g. 8GB, 16GB, 32GB)${NC}"
fi
echo -n "  Enter value [default: 8GB]: "
read -r INPUT_MEM
WSL_MEMORY="${INPUT_MEM:-8GB}"

# Prompt for CPU cores
if [ -n "$CURRENT_CPU" ]; then
    echo -e "  ${BOLD}CPU cores${NC} ${DIM}(current: ${CURRENT_CPU})${NC}"
else
    echo -e "  ${BOLD}CPU cores${NC} ${DIM}(e.g. 4, 8, 16)${NC}"
fi
echo -n "  Enter value [default: 4]: "
read -r INPUT_CPU
WSL_PROCESSORS="${INPUT_CPU:-4}"

echo ""
log "Writing .wslconfig вҶ’ memory=${WSL_MEMORY}, processors=${WSL_PROCESSORS}, networkingMode=mirrored"

cat > "$WSLCONFIG" <<EOF
[wsl2]
memory=${WSL_MEMORY}
processors=${WSL_PROCESSORS}

[experimental]
networkingMode=mirrored
EOF

ok "memory=${WSL_MEMORY}  |  processors=${WSL_PROCESSORS}  |  networkingMode=mirrored"
warn "Run 'wsl --shutdown' from Windows PowerShell to apply these changes"

# в”Җв”Җ Directories в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "Directories"
log "Creating ~/local workspace..."
mkdir -p ~/local/{sources,tools,vault,scratch}
ok "~/local/{sources, tools, vault, scratch}"

# в”Җв”Җ System Update & Base Deps в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "System Packages"
log "Updating apt..."
sudo apt-get update -y -q
sudo apt-get upgrade -y -q
log "Installing build essentials and dev libraries..."
sudo apt-get install -y -q \
    curl wget git build-essential cmake unzip zip \
    apt-transport-https ca-certificates gnupg lsb-release software-properties-common \
    libssl-dev zlib1g-dev libbz2-dev libreadline-dev libsqlite3-dev \
    libncursesw5-dev xz-utils tk-dev libxml2-dev libxmlsec1-dev libffi-dev liblzma-dev
ok "git $(git --version | awk '{print $3}')  |  cmake $(cmake --version | head -1 | awk '{print $3}')"

# в”Җв”Җ GitHub CLI в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "GitHub CLI"
log "Adding GitHub CLI apt repository..."
curl -fsSL https://cli.github.com/packages/githubcli-archive-keyring.gpg \
    | sudo dd of=/usr/share/keyrings/githubcli-archive-keyring.gpg 2>/dev/null
sudo chmod go+r /usr/share/keyrings/githubcli-archive-keyring.gpg
echo "deb [arch=$(dpkg --print-architecture) signed-by=/usr/share/keyrings/githubcli-archive-keyring.gpg] https://cli.github.com/packages stable main" \
    | sudo tee /etc/apt/sources.list.d/github-cli.list > /dev/null
sudo apt-get update -y -q
log "Installing gh..."
sudo apt-get install -y -q gh
ok "$(gh --version | head -1)"

# в”Җв”Җ Rustup в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "Rust"
if ! command -v rustup &>/dev/null; then
    log "Installing Rustup..."
    curl --proto '=https' --tlsv1.2 -sSf https://sh.rustup.rs | sh -s -- -y --no-modify-path
else
    log "Rustup already installed вҖ” skipping"
fi
source "$HOME/.cargo/env"
ok "$(rustc --version)  |  cargo $(cargo --version | awk '{print $2}')"

# в”Җв”Җ NVM в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "NVM + Node.js LTS"
NVM_VERSION="v0.40.1"
if [ ! -d "$HOME/.nvm" ]; then
    log "Installing NVM ${NVM_VERSION}..."
    curl -o- "https://raw.githubusercontent.com/nvm-sh/nvm/${NVM_VERSION}/install.sh" | bash
else
    log "NVM already installed вҖ” skipping"
fi
export NVM_DIR="$HOME/.nvm"
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"
ok "NVM $(nvm --version)"

log "Installing Node.js LTS..."
nvm install --lts
nvm use --lts
nvm alias default 'lts/*'
ok "Node $(node --version)  |  npm $(npm --version)"

# в”Җв”Җ Bun в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "Bun"
if ! command -v bun &>/dev/null; then
    log "Installing Bun..."
    curl -fsSL https://bun.sh/install | bash
else
    log "Bun already installed вҖ” skipping"
fi
export BUN_INSTALL="$HOME/.bun"
export PATH="$BUN_INSTALL/bin:$PATH"
ok "Bun $(bun --version)"

# в”Җв”Җ .NET SDK (LTS) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section ".NET SDK + Runtime"
export DOTNET_ROOT="$HOME/.dotnet"
export PATH="$PATH:$DOTNET_ROOT:$DOTNET_ROOT/tools"
if ! command -v dotnet &>/dev/null; then
    log "Downloading dotnet-install.sh..."
    curl -sSL https://dot.net/v1/dotnet-install.sh -o /tmp/dotnet-install.sh
    chmod +x /tmp/dotnet-install.sh
    log "Installing .NET SDK (LTS)..."
    /tmp/dotnet-install.sh --channel LTS
    log "Installing .NET Runtime (LTS)..."
    /tmp/dotnet-install.sh --channel LTS --runtime dotnet
else
    log ".NET already installed вҖ” skipping"
fi
ok ".NET SDK $(dotnet --version)"

# в”Җв”Җ pyenv в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
section "pyenv"
if [ ! -d "$HOME/.pyenv" ]; then
    log "Installing pyenv..."
    curl https://pyenv.run | bash
else
    log "pyenv already installed вҖ” skipping"
fi
export PYENV_ROOT="$HOME/.pyenv"
export PATH="$PYENV_ROOT/bin:$PATH"
eval "$(pyenv init -)"
